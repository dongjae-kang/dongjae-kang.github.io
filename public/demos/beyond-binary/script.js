/* ---- Render ---- */
function renderCards() {
  const grid = document.getElementById("cardGrid");
  const html = CARDS.map((c) => {
    const t = TIER[c.tier];
    return `
      <article class="card tier-${c.tier}" data-id="${c.id}" data-binary="${c.binaryLabel ? 1 : 0}" tabindex="0" role="button" aria-label="View content credentials for ${c.name}">
        <div class="card-photo">
          <img src="${c.image}" alt="AI-generated portrait used as a placeholder for ${c.name}" loading="lazy" />
          <div class="label-binary">Generated by AI</div>
          <div class="badge-tier" aria-label="${t.label}">
            <span class="badge-mark" aria-hidden="true">${t.initial}</span>
            <span class="badge-text">${t.label}</span>
          </div>
        </div>
        <div class="card-body">
          <p class="card-name">${c.name}</p>
          <p class="card-headline">${c.headline}</p>
          <div class="card-actions">
            <button class="card-btn primary" type="button" tabindex="-1">Message</button>
            <button class="card-btn" type="button" tabindex="-1">Connect</button>
          </div>
        </div>
      </article>
    `;
  }).join("");
  grid.innerHTML = html;

  grid.querySelectorAll(".card").forEach((el) => {
    el.addEventListener("click", () => openCred(el.dataset.id));
    el.addEventListener("keydown", (e) => {
      if (e.key === "Enter" || e.key === " ") {
        e.preventDefault();
        openCred(el.dataset.id);
      }
    });
  });
}

/* ---- Toggle mode ---- */
function setupToggle() {
  const buttons = document.querySelectorAll(".toggle-btn");
  buttons.forEach((btn) => {
    btn.addEventListener("click", () => {
      buttons.forEach((b) => {
        b.classList.remove("is-active");
        b.setAttribute("aria-selected", "false");
      });
      btn.classList.add("is-active");
      btn.setAttribute("aria-selected", "true");
      const mode = btn.dataset.mode;
      document.body.classList.toggle("mode-proposed", mode === "proposed");
    });
  });
}

/* ---- Credentials panel ---- */
const tierColors = {
  verified: "#5a7a64",
  enhanced: "#6b8c7a",
  assisted: "#b08654",
  synthetic: "#9a5a4b",
};

function openCred(id) {
  const card = CARDS.find((c) => c.id === id);
  if (!card) return;
  const t = TIER[card.tier];
  const c = card.credentials;

  const captureRows = c.capture
    .map((r) => `<li><strong>${r.k}:</strong> ${r.v}</li>`)
    .join("");
  const editRows = c.edits
    .map((r) => `<li><strong>${r.k}:</strong> ${r.v}</li>`)
    .join("");

  const noteHtml = c.note
    ? `<p class="cred-meta" style="font-style:normal;color:var(--ink-soft);background:var(--bg);border-left:3px solid var(--tier-assisted);padding:10px 12px;border-radius:4px;margin-top:18px;">${c.note}</p>`
    : "";

  const body = `
    <p class="cred-eyebrow">Content Credentials</p>
    <h3 class="cred-title">${card.name}</h3>
    <p class="card-headline" style="margin:0 0 18px;color:var(--ink-mute);">${card.headline}</p>
    <div class="cred-tier-line">
      <span class="cred-tier-dot" style="background:${tierColors[card.tier]};"></span>
      ${t.label}
    </div>
    <p style="font-size:13px;color:var(--ink-soft);margin:0 0 8px;">${t.description}</p>

    <div class="cred-section">
      <h4>Source</h4>
      <ul class="cred-chain">${captureRows}</ul>
    </div>

    <div class="cred-section">
      <h4>Edits &amp; AI involvement</h4>
      <ul class="cred-chain">${editRows}</ul>
    </div>

    <div class="cred-section">
      <h4>Verification</h4>
      <p style="font-size:13px;color:var(--ink-soft);margin:0;">${c.signature}</p>
    </div>

    ${noteHtml}

    <p class="cred-meta">This is a prototype. In a production deployment, these credentials would be cryptographically signed under the C2PA open standard.</p>
  `;

  document.getElementById("credBody").innerHTML = body;
  document.getElementById("credPanel").classList.add("is-open");
  document.getElementById("credPanel").setAttribute("aria-hidden", "false");
  document.getElementById("credOverlay").hidden = false;
  document.body.style.overflow = "hidden";
}

function closeCred() {
  document.getElementById("credPanel").classList.remove("is-open");
  document.getElementById("credPanel").setAttribute("aria-hidden", "true");
  document.getElementById("credOverlay").hidden = true;
  document.body.style.overflow = "";
}

function setupCredPanel() {
  document.getElementById("credClose").addEventListener("click", closeCred);
  document.getElementById("credOverlay").addEventListener("click", closeCred);
  document.addEventListener("keydown", (e) => {
    if (e.key === "Escape") closeCred();
  });
}

/* ---- Init ---- */
document.addEventListener("DOMContentLoaded", () => {
  renderCards();
  setupToggle();
  setupCredPanel();
});
